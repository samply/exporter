package de.samply.explorer;

public class PivotIdentifierException extends Exception {

    public PivotIdentifierException(String message) {
        super(message);
    }

    public PivotIdentifierException(String message, Throwable cause) {
        super(message, cause);
    }

    public PivotIdentifierException(Throwable cause) {
        super(cause);
    }

    public PivotIdentifierException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

}
