package de.samply.core;

public class ExporterCoreException extends Exception{

  public ExporterCoreException() {
  }

  public ExporterCoreException(String message) {
    super(message);
  }

  public ExporterCoreException(String message, Throwable cause) {
    super(message, cause);
  }

  public ExporterCoreException(Throwable cause) {
    super(cause);
  }

}
