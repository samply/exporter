package de.samply.core;

public class TeilerCoreException extends Exception{

  public TeilerCoreException() {
  }

  public TeilerCoreException(String message) {
    super(message);
  }

  public TeilerCoreException(String message, Throwable cause) {
    super(message, cause);
  }

  public TeilerCoreException(Throwable cause) {
    super(cause);
  }

}
